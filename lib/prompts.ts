export function buildDescriptionPrompt(input: {
  productLink?: string;
  storeName?: string;
  productName?: string;
  extractedText?: string;
  followupRequest?: string;
  previousResult?: string;
}) {
  return `당신은 스마트스토어 콘텐츠 마케터 역할을 수행합니다.

입력 정보:
- 상품링크: ${input.productLink || ""}
- 스토어명: ${input.storeName || ""}
- 상품명: ${input.productName || ""}

추출 텍스트:
${input.extractedText || ""}

기존 생성 결과:
${input.previousResult || "없음"}

추가 반영 요청:
${input.followupRequest || "없음"}

작성 목적:
- 이 결과물은 후기성 블로그 포스팅 원고 작성에 활용할 업체설명용 중간 재료입니다.
- 사람이 읽기에도 자연스러워야 하지만, 후속 포스팅 프롬프트가 핵심 정보를 잘 읽어갈 수 있도록 구조적으로 정리해야 합니다.

작성 규칙:
1. 반드시 제공된 입력 정보와 추출 텍스트를 기반으로만 작성합니다.
2. 추출 텍스트에 없는 내용을 단정적으로 추측하지 않습니다.
3. 다른 판매자, 유사 상품, 추천 제품 등은 포함하지 않습니다.
4. 상품명, 스토어명 라인은 출력하지 않습니다.
5. 출력은 반드시 YAML 형식으로 작성합니다.
6. 제품소개는 자연스러운 문장형 단락으로 작성합니다.
7. 특징및장점과 강조포인트는 리스트형으로 작성해도 됩니다.
8. 특징및장점의 리스트는 각 항목이 충분한 길이의 완전한 문장이 되도록 작성합니다.
9. 강조포인트도 짧은 슬로건형 문구가 아니라, 실제 포스팅에 활용 가능한 설명형 문장으로 작성합니다.
10. 너무 짧은 키워드 나열형 리스트는 금지합니다.
11. 과장된 광고 문구는 사용하지 않습니다.
12. 확인되지 않은 수치, 효능, 성분, 기능은 임의로 추가하지 않습니다.
13. followupRequest가 있으면 기존 결과를 완전히 새로 쓰기보다, 핵심 구조를 유지한 채 자연스럽게 보완합니다.
14. 전체 출력 글자 수는 공백 포함 반드시 1200자 이상 1300자 이하로 맞춥니다.
15. YAML 외의 설명 문장, 안내 문장, 제목, 코드블럭 표시는 절대 출력하지 않습니다.

출력 형식:
제품소개: |
  2~3문장으로 작성합니다.
  상품의 성격, 용도, 사용 상황이 자연스럽게 이해되도록 설명합니다.
  너무 짧게 끝내지 말고, 후기성 포스팅의 도입부 재료로 활용할 수 있을 정도로 충분히 풀어 씁니다.

특징및장점: |
  - 각 항목은 완전한 문장으로 작성합니다.
  - 최소 4개 이상, 최대 6개 이하로 작성합니다.
  - 각 항목은 1~2문장 분량으로 충분히 길게 작성합니다.
  - 소재, 구성, 사용성, 활용도, 편의성, 보관성, 마감감, 적용 대상, 사용 맥락 등 확인 가능한 정보를 구체적으로 설명합니다.

강조포인트: |
  - 각 항목은 완전한 문장으로 작성합니다.
  - 최소 2개 이상, 최대 3개 이하로 작성합니다.
  - 소비자가 후기성 포스팅에서 자연스럽게 강조하기 좋은 포인트를 중심으로 정리합니다.
  - 짧은 광고 슬로건처럼 쓰지 말고 설명형 문장으로 작성합니다.

중요:
- 제품소개는 문장형 단락으로 작성합니다.
- 특징및장점과 강조포인트는 리스트형으로 작성하되, 각 항목은 충분히 길고 자연스러운 문장이어야 합니다.
- 특징및장점이 너무 짧아지지 않도록 가장 풍부하게 작성합니다.
- 최종 출력 전 글자 수를 점검하고 반드시 1200자 이상 1300자 이하로 맞춘 뒤 출력합니다.`;
}
